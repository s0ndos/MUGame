
public class Card {
	
	Color color;
	int value;
	
	public Card(Color color, int value) {
		super();
		this.color = color;
		this.value = value;
	}
	public Color getColor() {
		return color;
	}
	public void setColor(Color color) {
		this.color = color;
	}
	public int getValue() {
		return value;
	}
	public void setValue(int value) {
		this.value = value;
	}
	
	
	
}
